//-------------------------------------------------------------------------------------
bool FontLib_Get16X16FontData(const uint8_t *pu8Text, uint8_t *pu8Data)
{
	uint32_t u32Dex;

	if((!pu8Data) || (!pu8Text))
		return false;

	for(u32Dex=0; u32Dex<FONTLIB_16X16_MAXNUM; u32Dex++)
	{
		if((pu8Text[0] == g_cau8FontLib16X16Index[u32Dex << 1]) \
				&& (pu8Text[1] == g_cau8FontLib16X16Index[(u32Dex << 1) + 1]))
		{
			break;
		}
	}

	if(u32Dex >= FONTLIB_16X16_MAXNUM)
		return false;

	memcpy(pu8Data, g_cau8FontLib16X16[u32Dex], 32);

	return true;
}
