ErrorStatus ctrl_radiotube_init(void)
{
	ErrorStatus eRet;

	eRet = ss_radiotube_init(&g_cstRadiotubeCfg);

	return eRet;
}
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
void ctrl_radiotube_on(CTRL_RADIOTUBE_NUM eNum)
{
	uint8_t u8Dex;

	for(u8Dex=0; u8Dex<CTRL_RADIOTUBE_NUM_MAX; u8Dex++)
	{
		if((eNum >> u8Dex) & 0x00000001)
		{
			ss_radiotube_on(u8Dex);
		}
	}
}
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
void ctrl_radiotube_off(CTRL_RADIOTUBE_NUM eNum)
{
	uint8_t u8Dex;

	for(u8Dex=0; u8Dex<CTRL_RADIOTUBE_NUM_MAX; u8Dex++)
	{
		if((eNum >> u8Dex) & 0x00000001)
		{
			ss_radiotube_off(u8Dex);
		}
	}
}
